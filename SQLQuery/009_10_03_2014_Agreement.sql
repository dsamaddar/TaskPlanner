alter proc spGetExecutivesByFacilityType
@FacilityTypeID nvarchar(50)
as
begin
	Select distinct AI.ServiceManager as 'EmployeeID',EI.EmployeeName + ' (' + EI.EmpCode + ')' as 'EmployeeName' 
	from tblAgreementInfo AI left Join tblEmployeeInfo EI On AI.ServiceManager=EI.EmployeeID
	Where FacilityTypeID = @FacilityTypeID And AI.ServiceManager is not null
	order by EmployeeName
end

