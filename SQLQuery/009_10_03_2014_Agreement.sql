Create proc spGetClientListByFacSM
@FacilityTypeID nvarchar(50),
@ServiceManager nvarchar(50)
as
begin
	Select AI.ClientID,CI.ClientName from tblAgreementInfo AI left Join tblClientInfo CI On AI.ClientID=CI.ClientID
	Where FacilityTypeID = @FacilityTypeID And ServiceManager=@ServiceManager
	order by CI.ClientName
end

