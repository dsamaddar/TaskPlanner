alter proc spInsertAgreementInfo
@AgreementNo nvarchar(50),
@ClientID nvarchar(50),
@FacilityTypeID nvarchar(50),
@ServiceManager nvarchar(50),
@ULCBranchID nvarchar(50),
@EntryBy nvarchar(50)
as
begin
	Declare @AgreementInfoID nvarchar(50)
	Declare @CurrentAgreementInfoID numeric(18,0)
	Declare @AgreementInfoIDPrefix as nvarchar(2)

	set @AgreementInfoIDPrefix='A-'

begin tran
	
	select @CurrentAgreementInfoID = cast(PropertyValue as numeric(18,0)) from tblAppSettings where  PropertyName='CurrentAgreementInfoID'
	
	set @CurrentAgreementInfoID=isnull(@CurrentAgreementInfoID,0)+1
	Select @AgreementInfoID=dbo.generateID(@AgreementInfoIDPrefix,@CurrentAgreementInfoID,8)		
	IF (@@ERROR <> 0) GOTO ERR_HANDLER

	Insert into tblAgreementInfo(AgreementInfoID,AgreementNo,ClientID,FacilityTypeID,ServiceManager,ULCBranchID,EntryBy)
	Values(@AgreementInfoID,@AgreementNo,@ClientID,@FacilityTypeID,@ServiceManager,@ULCBranchID,@EntryBy)
	IF (@@ERROR <> 0) GOTO ERR_HANDLER

	update tblAppSettings set PropertyValue=@CurrentAgreementInfoID where PropertyName='CurrentAgreementInfoID'
	IF (@@ERROR <> 0) GOTO ERR_HANDLER

COMMIT TRAN
RETURN 0
ERR_HANDLER:
ROLLBACK TRAN
RETURN 1
end

